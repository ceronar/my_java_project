package ch01_variable_operator;

public class Add2 {
    public static void main(String[] args) {
        int a = 3;
        int b = 4;
        int c = 5;
        int result = 2 * a + 3 * b - c;

        System.out.println("a = " + a +
                        "\nb = " + b +
                        "\nc = " + c +
                        "\nresult = " + result);
    }
}
